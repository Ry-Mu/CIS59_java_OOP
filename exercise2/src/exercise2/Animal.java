public class Animal {
    private String name;
    private int age;
    private String type;
    private String speak;
    
    public void setName(String n)
    {
        name = n;
    }
    
    public void setAge(int a)
    {
        age = a;
    }
    
    public void setType(String t)
    {
        type = t;
    }
    
    public void setSpeak(String s)
    {
        speak = s;
    }
    
    public String getName()
    {
        return name;
    }
    
    public int getAge()
    {
        return age;
    }
    
    public String getType()
    {
        return type;
    }
    
    public String speak()
    {
        return speak;
    }
            
            
    
}
